    public void buscar(int actual)
    {
        try {
            Ciclista p = ser.consultarCiclista(actual);
            
            if(p != null)
            {
                txtNombre1.setText(p.getNombre());
                txtEquipo1.setText(p.getEquipo());
            }
            else
            {
                txtNumeroAnt.setText("");
                txtNombre1.setText("");
                txtEquipo1.setText("");
                JOptionPane.showMessageDialog(null,"El numero actual no se encuentra en la base de datos!");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(GUIActualizarCiclista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void actualizar( int actual)
    {           
        try {
            if(!ser.buscarPorNumero(actual))
            {
                JOptionPane.showMessageDialog(null,"El numero actual no se encuentra en la base de datos!");
            } 
            else
            {
                Ciclista x = ser.buscarPorCiclista(actual);
                String nombre = txtNombre.getText().trim();
                String equipo = txtEquipo.getText().trim();
                
                if(nombre.equals("")&&equipo.equals(""))
                {
                    JOptionPane.showMessageDialog(null,"No se han hecho cambios al ciclista");
                }
                else
                {
                    if(!nombre.equals(""))
                    {
                        x.setNombre(nombre);
                    }
                    
                    if(!equipo.equals(""))
                    {
                        x.setEquipo(equipo);
                    }
                    
                    ser.actualizarCiclista(actual, x);
                    JOptionPane.showMessageDialog(null,"Se ha actualizado!");
                }

            }
        } catch (RemoteException ex) {
            Logger.getLogger(GUIActualizarCiclista.class.getName()).log(Level.SEVERE, null, ex);  
        }
    }

